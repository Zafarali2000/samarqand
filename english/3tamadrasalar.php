<p>It is a famous historical monument located in the city of Samarkand, Uzbekistan.
    <b>Some information about Ulugbek Madrasah:</b>
   <b> Construction: </b> Ulugbek madrasa was built by the famous Timurid ruler and astronomer Ulugbek in the 15th century (1417-1420). Ulugbek is known for his contributions to astronomy and mathematics, and he founded the madrasa as an educational institution.
</p>
<p><b>Architectural style: </b> The madrasa has a traditional Timurid architectural style. It has a rectangular shape with a central courtyard. The entrance portal is decorated with beautiful geometric patterns, calligraphy and intricate tiling. Blue and turquoise tiles are displayed on the facade of the madrasa, adding to its aesthetic appeal. <br>
    <b>Astronomical importance: </b> Ulugbek was a famous astronomer, and the madrasa integrated an observatory in its building. This observatory, known as the Ulugbek Observatory, was one of the most advanced observatories of its time. There was a large quadrant in which Ulugbek made accurate astronomical observations and compiled a star catalog known as "Zij-i Sultani".</p>
    <p><b> Usage: </b>Ulugbek madrasah includes lecture rooms, student rooms and a mosque. The rooms surrounding the central courtyard were used as classrooms for religious education. The mosque, located on the southern edge of the madrasa, served as a place of pilgrimage for students and local residents.<br>
    <b> Restoration:</b> Over time, the Ulugbek madrasa became unusable. However, extensive restoration work was carried out in the 20th century to preserve its architectural and historical significance. Restoration work is aimed at preserving the original Timurid style while ensuring structural stability. Today, the Ulughbek madrasa is a popular tourist attraction in Samarkand, attracting visitors with its historical significance and architectural beauty. . Studying the madrasa allows you to get acquainted with the rich cultural and educational heritage of the Timurid era. Additionally, the nearby Ulugbek Observatory bears witness to Ulugbek's scientific achievements. It's always a good idea to check for the latest information and any specific guidelines or restrictions.</p>
