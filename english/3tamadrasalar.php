<h3>Name of historical monument: Ulugbek madrasa</h3>
    <p>In the middle of the 15th century, the grandson of Amir Temurn, Mirza Ulugbek (1409-1449), came to power in Samarkand. It was Ulugbek who actively built educational institutions in Bukhara, Samarkand, Shahrisabz and Gijduvan. The Ulugbek madrasa in Samarkand is a classic example of the Islamic higher educational institutions of the East.
    </p>
