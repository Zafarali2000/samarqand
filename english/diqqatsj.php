<h3>Afrosiob Museum</h3>
    <p>Samarkand city history museum was founded in 1970. The museum is located in the town of Afrosiyob - the ancient part of Samarkand, before the conquest of the Mongols. In the exhibition halls of the museum, exhibits illuminating the history of the city from the middle of the first millennium BC to the beginning of the XIII century are displayed. Samarkand for two thousand years (So 'g'd or Sogdiya) is the central city, and the exhibits displayed in the museum clearly illuminate the high-level cultural, political, religious and industrial life of Samarkand.
    </p>
  
</div>
<div class="image">
<img src="images/Desktop/afrosiyob-muzeyi/muzey2.jpg" alt="rasm topilmadi">

</div>


<div class="content">
   <p>The museum contains rich collections of ceramics, glass, stone, metal, bone, corapilastics and architectural patterns that highlight the material culture of Samarkand. These are more than 20,000 artefacts. The main part of these exhibits is displayed in the expositions of the museum, which consists of 11 halls, and the rest of the auxiliary materials are stored in the museum funds. The Museum of the History of the City of Samarkand, located in the eastern part of the ancient city of Afrosiyob, was established in 1970 by the initiative of Sharif Rashidov. Dated October 24.
   
   </p>

