       <p> <b>In the third hall</b> items related to the Hellenistic period found their place, they are Samarkand's mill. avv. There are models of defensive walls built in the III-II centuries, weapons, coins, pottery, terracotta, stone and wooden objects, as well as fragments of wooden carvings taken from the fortress wall. <br>
       Objects illuminating the spiritual life and livelihood of the Samarkand people in the 1st-4th centuries AD have taken their place in the fourth hall, among which the altar-hearth of the 4th century AD, terracotta figurines, household items, and a workshop with bones are displayed. <br><b> In the fifth hall</b> objects related to fire and ancestral spirit belief: ossuaries, prayer part of the house, murals, wooden sculptures-pillars in the shape of a dancer, objects with Sogdian script - leather (envelope) , hum, porsang. At the same time, household items made of iron, bone and ceramics have found their place.
