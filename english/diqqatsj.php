       <p>In the <b>first hall</b> of the museum, there is a relief map showing the materials on the history of the archaeological study of the ruins of Afrosiyab up to the middle of the 19th century, the archaeological objects and administrative monuments of the city.
          <br>
          <b>In the second hall</b> there are objects related to the history of the first centuries of city life: a model of a pottery kiln, mill. avv. Pottery, stone and metal objects of VI-IV centuries are placed.

